import Head from "next/head";
import Button from "./Button";
import classes from "./css/event-result-title.module.css";

type Props = {
  date: Date;
};

export default function FilteredEventsTitle(props: Props) {
  const { date } = props;

  const humanReadableDate = new Date(date).toLocaleDateString("en-Us", {
    month: "long",
    year: "numeric",
  });

  return (
    <>
      <Head>
        <title>Event in {humanReadableDate}</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <section className={classes.title}>
        <h1>Events Available in {humanReadableDate}</h1>
        <Button link="/events">Back to All Events</Button>
      </section>
    </>
  );
}
